    private void atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarActionPerformed
    for(int i=0;i<BancodeDados.veiculos.size();i++){ 
       if(BancodeDados.veiculos.get(i).getPlaca().equals(txtplaca.getText())){ 
            BancodeDados.veiculos.get(i).setAno(txtano.getText()); 
            BancodeDados.veiculos.get(i).setMarca(txtmarca.getText()); 
            BancodeDados.veiculos.get(i).setModelo(txtmodelo.getText()); 
            BancodeDados.veiculos.get(i).setKm(txtkm.getText()); 
            BancodeDados.veiculos.get(i).setDisponibilidade("Dispon√≠vel"); 
            BancodeDados.veiculos.get(i).setCor(txtCor.getText()); 
            BancodeDados.veiculos.get(i).setFabricante(txtfabri.getText()); 
       } 
    }
        
    }//GEN-LAST:event_atualizarActionPerformed
       
    private void removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerActionPerformed
         for(int i=0;i<BancodeDados.veiculos.size();i++){  
              if(BancodeDados.veiculos.get(i).getPlaca().equals(txtplaca.getText())){  
                  BancodeDados.veiculos.remove(BancodeDados.veiculos.get(i));
              }
         }

    }//GEN-LAST:event_removerActionPerformed

